  export function getAtomicNumber(elementName: string): number | null {
    // Normalizar el nombre (quitar espacios, convertir a minúsculas)
    const normalizedName = elementName.trim()
  
    // Buscar en la tabla periódica
    if (periodicTable[normalizedName] !== undefined) {
      return periodicTable[normalizedName]
    }
  
    // Si no se encuentra, intentar buscar por coincidencias parciales
    const keys = Object.keys(periodicTable)
    for (const key of keys) {
      if (normalizedName.includes(key) || key.includes(normalizedName)) {
        return periodicTable[key]
      }
    }
  
    // Si no se encuentra, devolver null
    return null
  }